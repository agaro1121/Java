package storage;

/**
 * User class that is used to map the information from the data source
 * @author anthony.garo
 *
 */
public class User {

	private int userID;
	private String password;
	private String firstname;
	private String lastname;
	private String username;
	private boolean verified;
	private String ticker;
	private String status;
	private String theme;
	private boolean admin = false;
	private boolean share_Holder = false;
	private boolean seManager = false;
	private boolean broker = false;
	
	public int getUserID() {
		return userID;
	}
	public String getPassword() {
		return password;
	}
	public String getFirstname() {
		return firstname;
	}
	public String getLastname() {
		return lastname;
	}
	public String getUsername() {
		return username;
	}
	public boolean isVerified() {
		return verified;
	}
	public String getTicker() {
		return ticker;
	}
	public String getStatus() {
		return status;
	}
	public String getTheme() {
		return theme;
	}
	public boolean isAdmin() {
		return admin;
	}
	public boolean isShare_Holder() {
		return share_Holder;
	}
	public boolean isSeManager() {
		return seManager;
	}
	public boolean isBroker() {
		return broker;
	}
	public void setUserID(int userID) {
		this.userID = userID;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}
	public void setLastname(String lastname) {
		this.lastname = lastname;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public void setVerified(boolean verified) {
		this.verified = verified;
	}
	public void setTicker(String ticker) {
		this.ticker = ticker;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public void setTheme(String theme) {
		this.theme = theme;
	}
	public void setAdmin(boolean admin) {
		this.admin = admin;
	}
	public void setShare_Holder(boolean share_Holder) {
		this.share_Holder = share_Holder;
	}
	public void setSeManager(boolean seManager) {
		this.seManager = seManager;
	}
	public void setBroker(boolean broker) {
		this.broker = broker;
	}

}